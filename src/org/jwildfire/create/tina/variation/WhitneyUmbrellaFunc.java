package org.jwildfire.create.tina.variation;

import org.jwildfire.create.tina.base.XForm;
import org.jwildfire.create.tina.base.XYZPoint;

public class WhitneyUmbrellaFunc extends SimpleVariationFunc implements SupportsGPU {
  private static final long serialVersionUID = 1L;

  @Override
  public void transform(FlameTransformationContext pContext, XForm pXForm, XYZPoint pAffineTP, XYZPoint pVarTP, double pAmount) {
    /* whitney_umbrella by Don Town */
    double u = pAffineTP.x;
    double v = pAffineTP.y;
    pVarTP.x += pAmount * u * v;
    pVarTP.y += pAmount * u;
    pVarTP.z += pAmount * v * v;
  }

  @Override
  public String getName() {
    return "whitney_umbrella";
  }

  @Override
  public VariationFuncType[] getVariationTypes() {
    return new VariationFuncType[]{VariationFuncType.VARTYPE_3D, VariationFuncType.VARTYPE_SUPPORTS_GPU, VariationFuncType.VARTYPE_SUPPORTED_BY_SWAN};
  }

  @Override
  public String getGPUCode(FlameTransformationContext context) {
    return "float u = __x;\n"
        + "float v = __y;\n"
        + "__px += __whitney_umbrella * u * v;\n"
        + "__py += __whitney_umbrella * u;\n"
        + "__pz += __whitney_umbrella * v * v;\n";
  }
}
